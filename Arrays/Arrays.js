const strings =  ['a', 'b', 'c', 'd'];

// 4*4 =  16 bytes of storage

// push - adds something at the end of the array
strings.push('e') // O(1)


// pop - removes last item from the list
strings.pop();
strings.pop(); // O(1)


//unshift - inserts item at the start of the array 
strings.unshift()


console.log(strings)